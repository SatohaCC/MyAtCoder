import io
import sys

_INPUT = """\
3 6

"""
sys.stdin = io.StringIO(_INPUT)

# ---------------------------------------------------------------------------------------------------------
#  ３重ループを２重ループで
#
# ---------------------------------------------------------------------------------------------------------

n, k = map(int, input().split())

ans = 0

for a in range(1, n + 1):
    for b in range(1, n + 1):
        if 0 < (k - a - b) <= n :
            ans += 1

print(ans)
